def add(x, y):
  answer = x + y
  return answer

def subtract(x, y):
  answer = x - y
  return answer

def multiply(x, y):
  answer = x * y
  return answer

def divide(x, y):
  answer = x / y
  return answer

def exp(x, y):
  answer = x ** y
  return answer

print(add(5, 4))
print(subtract(5, 4))
print(multiply(5, 4))
print(divide(5, 4))
print(exp(5, 4))
