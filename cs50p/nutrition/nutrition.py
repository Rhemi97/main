def main():
    item = input("Item: ").casefold()

    if item == "apple":
        print("Calories: 130")

    elif item == "banana":
        print("Calories: 110")

    elif item == "avocado":
        print("Calories: 50")

    elif item == "kiwifruit":
        print("Calories: 90")

    elif item == "pear":
        print("Calories: 100")

    elif item == "sweet cherries":
        print("Calories: 100")

    else:
        print("")

main()


